a=92.5
print(f"{a:e}") #Formato notación exponencial (minúscula)
print(f"{a:E}") #Formato notación exponencial (mayúscula)
print(f"{a:f}") #Formato de punto fijo con 6 dígitos decimales
print(f"{a:}") #Sin especificador, el valor no se altera
print(f"{a:%}") #Multiplica el valor por 100 y agrega el %